# ----------------------------------------------------------------------
# | Math Changes in Python 3.13
# ----------------------------------------------------------------------
from math import fma


# ----------------------------------------------------------------------
# | Fused multiply-add operation.
# ----------------------------------------------------------------------
FUSED_OPERATION = fma(1, 2, 3)

def fused_operation(
    x: float | int,
    y: float | int,
    z: float | int
):
    return fma(x, y, z)

# ----------------------------------------------------------------------
# Example usage
# ----------------------------------------------------------------------
if __name__ == "__main__":
    print(FUSED_OPERATION)
    print(fused_operation(1,2,3))
