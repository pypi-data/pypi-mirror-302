    UnsafeCastToUint256: InstanceTestUnsafeCastToUint256ContractError

    UnsupportedToken: InstanceTestUnsupportedTokenContractError

    WhaleBalanceExceeded: InstanceTestWhaleBalanceExceededContractError

    WhaleIsContract: InstanceTestWhaleIsContractContractError

    def __init__(
        self,
    ) -> None:
        self.ExpInvalidExponent = InstanceTestExpInvalidExponentContractError()
        self.InsufficientLiquidity = (
            InstanceTestInsufficientLiquidityContractError()
        )
        self.InvalidTimestamp = InstanceTestInvalidTimestampContractError()
        self.LnInvalidInput = InstanceTestLnInvalidInputContractError()
        self.UnsafeCastToInt256 = InstanceTestUnsafeCastToInt256ContractError()
        self.UnsafeCastToUint256 = (
            InstanceTestUnsafeCastToUint256ContractError()
        )
        self.UnsupportedToken = InstanceTestUnsupportedTokenContractError()
        self.WhaleBalanceExceeded = (
            InstanceTestWhaleBalanceExceededContractError()
        )
        self.WhaleIsContract = InstanceTestWhaleIsContractContractError()

        self._all = [
            self.ExpInvalidExponent,
            self.InsufficientLiquidity,
            self.InvalidTimestamp,
            self.LnInvalidInput,
            self.UnsafeCastToInt256,
            self.UnsafeCastToUint256,
            self.UnsupportedToken,
            self.WhaleBalanceExceeded,
            self.WhaleIsContract,
        ]

    def decode_custom_error(self, data: str) -> tuple[Any, ...]:
        """Decodes a custom contract error."""
        selector = data[:10]
        for err in self._all:
            if err.selector == selector:
                return err.decode_error_data(HexBytes(data[10:]))

        raise ValueError(
            f"InstanceTest does not have a selector matching {selector}"
        )


