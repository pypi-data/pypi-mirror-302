            info = '         --- {:16s} - {:8s}:'.format(self.__MergeGetObjectNameForOutput(self.__Decode(obj)), str(obj))
            info += 'SI = {:4.2f} - modifications: '.format(SI)
            for (i, change) in enumerate(changes):
                if i < (len(changes) - 1):
                    info += '{:1},'.format(change)
                else:
                    info += '{:1}'.format(change)
            if not changes:
                info += 'none'
            self.logger.Debug(info)

